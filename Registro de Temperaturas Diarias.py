def calcular_promedios_semanales(matriz_temperaturas):
    promedios_semanales = []
    for ciudad in matriz_temperaturas:
        promedios_ciudad = []
        for semana in ciudad:
            suma_temperaturas_semana = sum(dia["temp"] for dia in semana)
            promedio_semana = suma_temperaturas_semana / len(semana)
            promedios_ciudad.append(promedio_semana)
        promedios_semanales.append(promedios_ciudad)
    return promedios_semanales


def imprimir_promedios(promedios_ciudad):
    for idx_semana, promedio_semana in enumerate(promedios_ciudad, start=1):
        print(f"Semana {idx_semana}: {promedio_semana:.2f}")


